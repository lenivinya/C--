/* Напишите программу, которая продемонстрирует как работают
операторы -- и ++. */

#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Введите два значения для демонстрации примера: \n";
    cout << "a: ";
    cin >> a;
    cout << "b: ";

    cin >> b;

    cout << "Начальные значения: a = " << a << ", b = " << b << endl;

    // префиксный инкремент
    cout << "++a = " << ++a << " (сначала увеличили a, потом вывели)" << endl;

    // постфиксный инкремент
    cout << "b++ = " << b++ << " (сначала вывели b, потом увеличили)" << endl;
    cout << "Теперь b = " << b << endl;

    // префиксный декремент
    cout << "--a = " << --a << " (сначала уменьшили a, потом вывели)" << endl;

    // постфиксный декремент
    cout << "b-- = " << b-- << " (сначала вывели b, потом уменьшили)" << endl;
    cout << "Теперь b = " << b << endl;

    return 0;
}